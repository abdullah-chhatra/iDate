import Foundation

public extension NSDate {
    
    public class var now : NSDate {
        return NSDate()
    }
    
    public class var dayAfterNow: NSDate {
        return NSDate()
    }
    
    public class var dayBeforeNow: NSDate {
        return NSDate()
    }
    
    public class var today: NSDate {
        return NSDate()
    }
    
    public class var yesterday:  NSDate {
        return NSDate()
    }
    
    public class var tomorrow:  NSDate {
        return NSDate()
    }
    
}